    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        int codigo = Integer.parseInt(txtLibro.getText());
        oLibro = oControlLib.BuscarUno(codigo);
        
        boolean estado = oLibrosCola.verificaCapacidad();
        if(estado == true){
            oLibrosCola.insertaLibro(oLibro);
        }else{
            JOptionPane.showMessageDialog(this, "La cola ha alcanzado su capacidad m√°xima", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        oLibrosCola.limpiarTabla(modelo);
        oLibrosCola.mostrarTabla(modelo);
        
        
        //System.out.println(oLibrosCola.muestraLibros());    
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        int n = Integer.parseInt(txtCant.getText());
        oLibrosCola=new ColaPrioridad(n);
        pnlDatos.setVisible(true);
    }//GEN-LAST:event_btnCrearActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        oLibrosCola.eliminaElemento();
        oLibrosCola.limpiarTabla(modelo);
        oLibrosCola.mostrarTabla(modelo);
    }//GEN-LAST:event_btnBorrarActionPerformed

